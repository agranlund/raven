bool vbr_Init()
{
    vbrTable = (uint32_t*) mem_LinearAlloc(256 * 4, 256);
#if VBR_PROXY_IN_ROM
    for (uint32_t i=0; i<256*4; i+=4) {
        vbr_Set(i, (uint32_t) &vbrProxy[i]);
    }
#else
    vbrProxy = (uint16_t*) mem_LinearAlloc(256 * 4 * 2, 256);
    for (uint32_t i=0, j=0; i<256*4; i+=4) {
        vbrProxy[j++] = 0x4ef0; // jmp ([addr.w])
        vbrProxy[j++] = 0x01e1;
        vbrProxy[j++] = (uint16_t)i;
        vbrProxy[j++] = 0x0000; // unused
        vbr_Set(i, (uint32_t) &vbrProxy[j-4]);
    }
#endif
    vbr_Set(0x60, (uint32_t) vecRTE); // IRQ0 : Spurious
    vbr_Set(0x7C, (uint32_t) vecNMI); // IRQ7 : NMI
    vbr_Apply();
    return true;
}

void vbr_Set(uint32_t vec, uint32_t addr)
{
    vbrTable[vec >> 2] = addr;
}

void vbr_Apply()
{
    cpu_SetVBR((uint32_t)vbrTable);
}


//-----------------------------------------------------------------------
// motorola support package
//
//  Installed to vbr proxy by defaults.
//  can be reinstalled to different table later by calling msp_Install
//
//  eg, rvbios.prg can call msp_Install(0) to put them in TOS vectors
//  so that some programs (mint1.16) which use unimpl instructions
//  to identify cpu can do so correctly.
//
//-----------------------------------------------------------------------
extern void cpu_InstallSP(uint32_t vbr);

bool msp_ins;
uint32_t msp_vbr;
uint32_t msp_bak[10];

void msp_Backup(uint32_t vbr) {
    msp_bak[0] = *((uint32_t*)(vbr + 0xf4));
    msp_bak[1] = *((uint32_t*)(vbr + 0xd8));
    msp_bak[2] = *((uint32_t*)(vbr + 0xd0));
    msp_bak[3] = *((uint32_t*)(vbr + 0xd4));
    msp_bak[4] = *((uint32_t*)(vbr + 0xcc));
    msp_bak[5] = *((uint32_t*)(vbr + 0xc8));
    msp_bak[6] = *((uint32_t*)(vbr + 0xc4));
    msp_bak[7] = *((uint32_t*)(vbr + 0x2c));
    msp_bak[8] = *((uint32_t*)(vbr + 0xdc));
    msp_bak[9] = *((uint32_t*)(vbr + 0xf0));
}

void msp_Restore(uint32_t vbr) {
    *((uint32_t*)(vbr + 0xf4)) = msp_bak[0];
    *((uint32_t*)(vbr + 0xd8)) = msp_bak[1];
    *((uint32_t*)(vbr + 0xd0)) = msp_bak[2];
    *((uint32_t*)(vbr + 0xd4)) = msp_bak[3];
    *((uint32_t*)(vbr + 0xcc)) = msp_bak[4];
    *((uint32_t*)(vbr + 0xc8)) = msp_bak[5];
    *((uint32_t*)(vbr + 0xc4)) = msp_bak[6];
    *((uint32_t*)(vbr + 0x2c)) = msp_bak[7];
    *((uint32_t*)(vbr + 0xdc)) = msp_bak[8];
    *((uint32_t*)(vbr + 0xf0)) = msp_bak[9];
}

void msp_Install(uint32_t vbr) {
    if (msp_ins && (msp_vbr != vbr)) {
        msp_Restore(msp_vbr);
    }
    msp_Backup(vbr);
    cpu_InstallSP(vbr);
    vbr_Apply();
    msp_vbr = vbr;
    msp_ins = true;
}

bool msp_Init() {
    uint32_t vbr = cpu_GetVBR();
    if (vbr == 0) {
        return false;
    }
    msp_Install(vbr);
    return true;
}
