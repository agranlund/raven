//-----------------------------------------------------------------------
// cpu
//-----------------------------------------------------------------------
void cpu_NullNMI(regs_t* r) {
}


bool cpu_Init()
{
    // set dummy nmi handler
    NMIFunc = cpu_NullNMI;
    NMIBusy = 0;

    uint32_t rev;
    cpu_Detect(&rev, 0);

    uint32_t pcr = 0;
    pcr |= (1 << 0);        // enable superscalar dispatch
    if ((rev == 1) || (rev == 5)) {
        pcr |= (1 << 5);    // disable store/load bypass (errata I14)
    }
    cpu_SetPCR(pcr);
    return true;
}


uint32_t cpu_Detect(uint32_t* revout, uint32_t* idout)
{
    uint32_t sku = CPU_UNKONWN;
    uint32_t rev = 1;

    uint32_t pcr = cpu_GetPCR();
    rev = (pcr >> 8) & 0xFF;
    if ((pcr & 0x000f0000) == 0)
        sku = CPU_68060;
    else if (rev == 4)
        sku = CPU_68LC060;
    else if (rev == 3)
        sku = CPU_68EC060;

    if (idout)
        *idout = pcr & 0xffffff00;

    if (revout)
        *revout = rev;

    return sku;
}


NMIFunc_t cpu_SetNMI(NMIFunc_t func)
{
    NMIFunc_t old = NMIFunc;
    NMIFunc = func;
    return old;
}


